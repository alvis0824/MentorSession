package mentorSession0601;

public class DayInAWeek {

    public static void main(String[] args) {

        for (int i = 1; i < 5; i++) {

            System.out.println("Week: " + i);

            for (int j = 1; j < 8; j++) {

                System.out.println("  Day: " + j);
            }
        }
    }
}
